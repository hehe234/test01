"""
变量的命名规则：
    1.强制性：如果违背了强制性的规定，运行程序就会报错
        1.1 变量的命名首字母不能以数字开头
        1.2 变量的命名不能使用关键字或保留字(因为在编程语言中，有些单词是具有特殊含义，所以我们在定义变量的时候尽量不要冲突了)
        1.3 变量的命名中不能有特殊的符号
        1.4 变量的首字母可以是下划线(_)，字母，后面的内容可以是数字或字母
    2.应该要遵守的规则：（不是必须的，但是建议咱们按照这个规则来遵守）
        2.1 变量的命名尽量做到见明知意（我们通过这个变量名就能大致知道这个 变量是干什么用途的）
            name="张三"
            age=18
            address="长沙"
        2.2 变量的命名如果是由多个单词组成，那么我们可以使用驼峰命名法
            小驼峰： 第一个单词首字母消协，后面合成的单词首字母都答谢
            大驼峰： 每个单词的首字母都大写
            下划线连接：在拼接的单词前面添加一个_(下划线)
            
            小驼峰： 用户名：user+name => userName
            大驼峰： UserName
            下划线： user_name
"""

userName="王五"
user_name="赵六"
print(userName,user_name)
