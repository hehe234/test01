# python是一个弱类型的编程语言，java是强类型的编程语言
# 在python定义变量不需要先定义变量的数据类型，变量的数据类型是根据变量对应的值来确定
# 在Java中，定义变量时，是需要先定义变量的 数据类型，然后在给该变量 赋值，值必须要与变量定义的数据类型保持一致
a=10
b="hello"
c=12.5
d=True
# 在python中有一个查看变量的数据类型的方法，type(变量名/值)
# 在控制它输出语句，使用print(要输出的内容/变量)
# 在python输出语句print可以一次性输出多个不同的值/变量，使用,隔开
print(a,type(a))
print(b,type(b))
print(c,type(c))
print(d,type(d))

